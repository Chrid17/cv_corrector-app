import 'package:flutter/material.dart';
import 'package:flutter_animate/flutter_animate.dart';
import 'package:share_plus/share_plus.dart';
import '../../core/constants/app_colors.dart';
import '../../core/constants/app_text_styles.dart';
import '../../core/widgets/common_widgets.dart';
import '../../domain/entities/cv_analysis.dart';
import 'tabs/overview_tab.dart';
import 'tabs/corrections_tab.dart';
import 'tabs/keywords_tab.dart';
import 'tabs/cover_letter_tab.dart';
import 'tabs/interview_tab.dart';
import 'tabs/skills_tab.dart';
import 'tabs/proposed_changes_tab.dart';

class ResultScreen extends StatefulWidget {
  final CvAnalysis result;
  const ResultScreen({super.key, required this.result});

  @override
  State<ResultScreen> createState() => _ResultScreenState();
}

class _ResultScreenState extends State<ResultScreen> with TickerProviderStateMixin {
  late final TabController _tabController;

  final _tabs = const [
    'Overview',
    'Corrections',
    'Proposals',
    'Keywords',
    'Skills',
    'Cover Letter',
    'Interview',
  ];

  @override
  void initState() {
    super.initState();
    _tabController = TabController(length: _tabs.length, vsync: this);
  }

  @override
  void dispose() {
    _tabController.dispose();
    super.dispose();
  }

  @override
  Widget build(BuildContext context) {
    final r = widget.result;
    return Scaffold(
      backgroundColor: AppColors.background,
      appBar: AppBar(
        backgroundColor: AppColors.surface,
        title: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            Text(r.candidateName, style: AppTextStyles.headingMedium),
            Text(r.jobTitle, style: AppTextStyles.bodySmall.copyWith(color: AppColors.primary)),
          ],
        ),
        actions: [
          IconButton(
            icon: const Icon(Icons.share_outlined, color: AppColors.textSecondary),
            onPressed: () => _share(r),
          ),
          const SizedBox(width: 4),
        ],
        bottom: TabBar(
          controller: _tabController,
          isScrollable: true,
          indicatorColor: AppColors.primary,
          indicatorWeight: 2,
          labelColor: AppColors.primary,
          unselectedLabelColor: AppColors.textSecondary,
          labelStyle: AppTextStyles.label.copyWith(fontSize: 13),
          tabAlignment: TabAlignment.start,
          tabs: _tabs.map((t) => Tab(text: t)).toList(),
        ),
      ),
      body: TabBarView(
        controller: _tabController,
        children: [
          OverviewTab(result: r),
          CorrectionsTab(result: r),
          const ProposedChangesTab(),
          KeywordsTab(result: r),
          SkillsTab(result: r),
          CoverLetterTab(result: r),
          InterviewTab(result: r),
        ],
      ),
    );
  }

  void _share(CvAnalysis r) {
    final text = '''
CV Analysis Report — ${r.candidateName}
=============================
ATS Score: ${r.atsScore}/100
Career Level: ${r.careerLevel}

Summary:
${r.overallSummary}

Quick Wins:
${r.quickWins.map((q) => '• $q').join('\n')}

Generated by CV Analyzer
''';
    Share.share(text);
  }
}
